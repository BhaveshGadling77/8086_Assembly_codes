.MODEL SMALL
.STACK 100H

.DATA

NO1 DB 00H
NO2 DB 00H

MSG1 DB 10,13,"ENTER 8 BITS NUMBER: $" 
MSG2 DB 10,13,"RESULT IS: $"

.CODE
MOV AX, @DATA
MOV DS, AX

LEA CX, MSG1
CALL PRINT_STR


CALL ACCEPT
MOV NO1, BL 

LEA CX, MSG1
CALL PRINT_STR

CALL ACCEPT
MOV NO2, BL

LEA CX, MSG2
CALL PRINT_STR


MOV BL, NO1
ADD BL, NO2 


CALL PRINT

HLT

PRINT:
MOV CH, 02H
MOV CL, 04H


BA:ROL BL, CL 

MOV DL, BL
AND DL, 0FH

CMP DL, 0AH
JC L1
ADD DL, 07H
L1:ADD DL, 30H 

MOV AH, 02H
INT 21H 

DEC CH
JNZ BA
RET

 
      
ACCEPT:

MOV CH, 02H
MOV CL, 04H
MOV BL, 00H
BACK:SHL BL, CL

MOV AH, 01H
INT 21H


CMP AL, 41H 
JC L2:
SUB AL, 07H  
L2:SUB AL, 30H
              
MOV AH, 00H              
ADD BL, AL
DEC CH
JNZ BACK
RET

PRINT_STR:
MOV AH, 09H
MOV DL, CL
INT 21H
RET



END